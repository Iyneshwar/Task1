
import java.util.Scanner;

public class Main {
    public static void main(String[] args) {

        //for loop
        for (int i = 10; i <= 50; i++) {
            System.out.println(i);

        }

        System.out.println;

        //while loop
        int num = 10;
        while (num <= 50) {
            System.out.println(num);
            num++;
        }

    }
}